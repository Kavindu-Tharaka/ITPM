package tool.model;

public class Control {

	int lineNumber,line_weight,no_of_control,cspps;
	String line;
	/**
	 * @return the lineNumber
	 */
	public int getLineNumber() {
		return lineNumber;
	}
	/**
	 * @param lineNumber the lineNumber to set
	 */
	public void setLineNumber(int lineNumber) {
		this.lineNumber = lineNumber;
	}
	/**
	 * @return the line_weight
	 */
	public int getLine_weight() {
		return line_weight;
	}
	/**
	 * @param line_weight the line_weight to set
	 */
	public void setLine_weight(int line_weight) {
		this.line_weight = line_weight;
	}
	/**
	 * @return the no_of_control
	 */
	public int getNo_of_control() {
		return no_of_control;
	}
	/**
	 * @param no_of_control the no_of_control to set
	 */
	public void setNo_of_control(int no_of_control) {
		this.no_of_control = no_of_control;
	}
	/**
	 * @return the cspps
	 */
	public int getCspps() {
		return cspps;
	}
	/**
	 * @param cspps the cspps to set
	 */
	public void setCspps(int cspps) {
		this.cspps = cspps;
	}
	/**
	 * @return the line
	 */
	public String getLine() {
		return line;
	}
	/**
	 * @param line the line to set
	 */
	public void setLine(String line) {
		this.line = line;
	}
	
}
